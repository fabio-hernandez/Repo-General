    private void BtnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCargarActionPerformed

        String datos = AreaCarga.getText();
        String[] datoscargados = datos.split("\n");

        for (String info : datoscargados) {

            String[] datosfinales = info.split(",");

            switch (datosfinales[0]) {

                case "TIPO":
                    Tipo local = new Tipo(datosfinales[1], datosfinales[2]);
                    tipo[contadorTipo] = local;
                    contadorTipo++;
                    break;

                case "MODELO":
                    Modelo local2 = new Modelo(Integer.parseInt(datosfinales[1]), datosfinales[2]);
                    modelo[contadorModelo] = local2;
                    contadorModelo++;
                    break;

                case "MARCA":
                    Marca local3 = new Marca(datosfinales[1], datosfinales[2]);
                    marca[contadorMarca] = local3;
                    contadorMarca++;
                    break;

                case "LINEA":
                    Linea local4 = new Linea(datosfinales[1], datosfinales[2]);
                    linea[contadorLinea] = local4;
                    contadorLinea++;
                    break;

                case "USO":
                    Uso local5 = new Uso(datosfinales[1], datosfinales[2]);
                    uso[contadorUso] = local5;
                    contadorUso++;
                    break;

                case "MECANICA":
                    Mecanica local6 = new Mecanica(datosfinales[1], datosfinales[2]);
                    mecanica[contadorMecanica] = local6;
                    contadorMecanica++;
                    break;

                case "REPUESTO":
                    Repuesto local7 = new Repuesto(datosfinales[1], datosfinales[2]);
                    repuesto[contadorRepuesto] = local7;
                    contadorRepuesto++;
                    break;

            }

        }


    }//GEN-LAST:event_BtnCargarActionPerformed

