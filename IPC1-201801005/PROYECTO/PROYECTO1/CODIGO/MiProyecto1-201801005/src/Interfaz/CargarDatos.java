    public static int contadorTipo = 0;
    public static int contadorModelo = 0;
    public static int contadorMarca = 0;
    public static int contadorLinea = 0;
    public static int contadorUso = 0;
    public static int contadorMecanica = 0;
    public static int contadorRepuesto = 0;

