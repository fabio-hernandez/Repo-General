    public String ObtenerNombre(String Dpi) {
        String nombre = "";

        for (int i = 0; i < Solicitudes.seguro.length; i++) {

            if (Dpi.equals(Solicitudes.seguro[i].getDPI()) && Solicitudes.seguro[i] != null) {
                nombre = Solicitudes.seguro[i].getNombre();
                break;
            }
        }
        return nombre;
    }

    public String ObtenerApellido(String Dpi) {
        String apellido = "";

        for (int i = 0; i < Solicitudes.seguro.length; i++) {
            if (Dpi.equals(Solicitudes.seguro[i].getDPI()) && Solicitudes.seguro[i] != null) {
                apellido = Solicitudes.seguro[i].getApellido();
                break;
            }
        }
        return apellido;
    }

    public String ObtenerTelefon(String Dpi) {
        String telefono = "";

        for (int i = 0; i < Solicitudes.seguro.length; i++) {
            if (Dpi.equals(Solicitudes.seguro[i].getDPI()) && Solicitudes.seguro[i] != null) {
                telefono = Solicitudes.seguro[i].getTelefono();
                break;
            }
        }

        return telefono;
    }

